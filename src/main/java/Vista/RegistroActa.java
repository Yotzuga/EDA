    private void btnSellarActaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSellarActaActionPerformed

        if (mesaElectoral.getActaElectoral() == null) {
            JOptionPane.showMessageDialog(this, "Primero realice el Registro de Acta Electoral.");
            return;
        }

        try {
            // Asignar datos del formulario al acta electoral
            int votoBlanco = Integer.parseInt(txtVotoBlanco.getText().trim());
            int votoNulo = Integer.parseInt(txtVotoNulo.getText().trim());
            int votoTotal = Integer.parseInt(txtvotoTotal.getText().trim());
            String obs = txtObsv.getText().trim();

            mesaElectoral.getActaElectoral().setVotoBlanco(votoBlanco);
            mesaElectoral.getActaElectoral().setVotoNulo(votoNulo);
            mesaElectoral.getActaElectoral().setVotoTotal(votoTotal);
            mesaElectoral.getActaElectoral().setObs(obs);

            JOptionPane.showMessageDialog(this, "Acta Electoral sellada exitosamente.");

            // Generar informe completo por Consola
            imprimirInformeMesa();

            // Mostrar reporte automáticamente en la ventana ReporteMesa
            ReporteMesa ventanaReporte = new ReporteMesa(mesaElectoral); // Llamar al constructor con la mesa
            ventanaReporte.setVisible(true); // Mostrar la ventana con el reporte

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese valores válidos para los campos de votos.");
        }

    }//GEN-LAST:event_btnSellarActaActionPerformed

    private void imprimirInformeMesa() {
        System.out.println("----- INFORME GENERAL DE LA MESA ELECTORAL -----");
        System.out.println("Número de Mesa: " + mesaElectoral.getnMesa());
        System.out.println("Miembros de la Mesa:");
        mesaElectoral.getPilaMiebros().printConsola(); // Método para imprimir pila de miembros

        ActaElectoral acta = mesaElectoral.getActaElectoral();
        if (acta != null) {
            System.out.println("Acta Electoral:");
            System.out.println("Título: " + acta.getTitulo());
            System.out.println("Fecha: " + acta.getFecha());
            System.out.println("Número de Mesa: " + acta.getnMesa());
            System.out.println("Votantes Registrados: " + acta.getVotantesRegistradosAc());
            System.out.println("Votos Blancos: " + acta.getVotoBlanco());
            System.out.println("Votos Nulos: " + acta.getVotoNulo());
            System.out.println("Votos Totales: " + acta.getVotoTotal());
            System.out.println("Observaciones: " + acta.getObs());
            System.out.println("Candidatos en la Cola:");
            acta.getColaCandidatos().printConsola(); // Usa el método existente para imprimir los candidatos
        } else {
            System.out.println("No se ha registrado un acta electoral.");
        }
        System.out.println("------------------------------------------------");
    }

