    private void btnVotarCandidatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVotarCandidatoActionPerformed

        if (mesaElectoral.getActaElectoral() == null) {
            JOptionPane.showMessageDialog(this, "Primero debe registrar el Acta Electoral para esta mesa.");
            return;
        }
        // Ahora puedes acceder a la cola de candidatos
        String candidato = txtCandidatoBuscar.getText().trim();

        if (candidato.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del candidato.");
            return;
        }
        // Buscar candidato
        Candidato candidatoEncontrado = mesaElectoral.getActaElectoral().getColaCandidatos().returnCandidato_Nombre(candidato);
               
        if (candidatoEncontrado != null) {
            // Proceso normal
            int votos = Integer.parseInt(txtVotoCandidato.getText().trim());

            lbPartido.setText(candidatoEncontrado.getPartidoPolitico());

            candidatoEncontrado.setVotos(votos);
                      
            JOptionPane.showMessageDialog(this, "Votos añadidos con éxito.");
        } else {
            JOptionPane.showMessageDialog(this, "Candidato no encontrado.");
        }
    }//GEN-LAST:event_btnVotarCandidatoActionPerformed
