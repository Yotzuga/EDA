    public void ObtenerLabel() {
        MiembroMesa P = mesaElectoral.getPilaMiebros().getMiembroPorTipo("Presidente");
        MiembroMesa S = mesaElectoral.getPilaMiebros().getMiembroPorTipo("Secretario");
        MiembroMesa V = mesaElectoral.getPilaMiebros().getMiembroPorTipo("Vocal");
        lbNmesa.setText(mesaElectoral.getnMesa());
        lbPresidente.setText(P.getNombre() + " " + P.getTipoMienbro());
        lbSecretario.setText(S.getNombre() + " " + S.getTipoMienbro());
        lbVocal.setText(V.getNombre() + " " + V.getTipoMienbro());

        if (mesaElectoral.getActaElectoral() != null) {

            txtTitulo.setText(mesaElectoral.getActaElectoral().getTitulo());
            txtFecha.setText(mesaElectoral.getActaElectoral().getFecha());
            String VotantesRegist = String.valueOf(mesaElectoral.getActaElectoral().getVotantesRegistradosAc());
            txtvotantesRegistradosAc.setText(VotantesRegist);

            // Convertir los valores enteros a String y asignarlos a los campos de texto
            String VotoBlanco = String.valueOf(mesaElectoral.getActaElectoral().getVotoBlanco());
            String VotoNulo = String.valueOf(mesaElectoral.getActaElectoral().getVotoNulo());
            String VotoTotal = String.valueOf(mesaElectoral.getActaElectoral().getVotoTotal());
            String Obsv = mesaElectoral.getActaElectoral().getObs();

            // Configurar los textos de los campos de texto
            txtVotoBlanco.setText(VotoBlanco);
            txtVotoNulo.setText(VotoNulo);
            txtvotoTotal.setText(VotoTotal);
            txtObsv.setText(Obsv);
        } else {
            // Si no hay acta, los campos se quedan en blanco
            txtVotoBlanco.setText("");
            txtVotoNulo.setText("");
            txtvotoTotal.setText("");
            txtObsv.setText("");
        }

    }

