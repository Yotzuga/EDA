    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // Obtener datos de los campos
        String fecha = txtFecha.getText();
        String tipoEleccion = (String) cbxTipo.getSelectedItem();

        // Crear o actualizar la elección
        if (eleccion == null) {
            eleccion = new Elecciones(fecha, tipoEleccion);
            javax.swing.JOptionPane.showMessageDialog(this, "Elección creada exitosamente.");

        } else {
            eleccion.setFecha(fecha);
            eleccion.setTipoEleccion(tipoEleccion);
            javax.swing.JOptionPane.showMessageDialog(this, "Elección modificada exitosamente.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

