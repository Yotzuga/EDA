    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Eliminar la elección estática
        if (eleccion != null) {
            eleccion = null;
            txtFecha.setText("");
            cbxTipo.setSelectedIndex(0);
            javax.swing.JOptionPane.showMessageDialog(this, "Elección eliminada exitosamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No hay ninguna elección para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
