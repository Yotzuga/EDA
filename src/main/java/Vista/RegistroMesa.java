    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String nombreMesa = this.txtBusca.getText();
        MesaElectoral mesaBuscada = listaGeneral.returnMesa(nombreMesa);
        if (mesaBuscada != null) {
            PilaMienbrosMesa pilaMienbros = mesaBuscada.getPilaMiebros();
            // Verifica si la pila de miembros es null antes de intentar acceder a los miembros
            if (pilaMienbros != null) {
                MiembroMesa presidente = pilaMienbros.getMiembroPorTipo("Presidente");
                MiembroMesa secretario = pilaMienbros.getMiembroPorTipo("Secretario");
                MiembroMesa vocal = pilaMienbros.getMiembroPorTipo("Vocal");
                // Actualiza los labels según si se encontró o no un miembro
                this.lbPresidente.setText(presidente != null ? presidente.getNombre() + " " + presidente.getDni() : "No asignado");
                this.lbSecretario.setText(secretario != null ? secretario.getNombre() + " " + secretario.getDni() : "No asignado");
                this.lbVocal.setText(vocal != null ? vocal.getNombre() + " " + vocal.getDni() : "No asignado");
            } else {
                // Si la pila es null, muestra "No asignado" en los labels
                this.lbPresidente.setText("No asignado");
                this.lbSecretario.setText("No asignado");
                this.lbVocal.setText("No asignado");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Mesa no encontrada.");
            limpiar();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

