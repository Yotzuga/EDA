    public void gestionRegistro() {
        String nombreMesa = this.txtNumeroMesa.getText();
        // Busca la mesa específica en la listaGeneral
        MesaElectoral mesaSeleccionada = listaGeneral.returnMesa(nombreMesa);
        if (mesaSeleccionada != null) {
            // Pasa la mesa seleccionada al constructor de RegistroMienbroMesa
            RegistroMienbroMesa registroMiembro = new RegistroMienbroMesa(mesaSeleccionada);
            registroMiembro.setVisible(true); // Muestra la ventana para añadir miembros a la mesa seleccionada
        } else {
            JOptionPane.showMessageDialog(this, "Mesa no encontrada.");
        }
    }

