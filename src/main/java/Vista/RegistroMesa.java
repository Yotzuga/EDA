    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed

        String numeroMesa = txtNumeroMesa.getText().trim();
        if (numeroMesa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un número de mesa.");
            return;
        }
        if (listaGeneral.buscarNumeraMesa(numeroMesa)) {
            JOptionPane.showMessageDialog(this, "Esta mesa electoral ya existe.");
        } else {
            MesaElectoral nuevaMesa = new MesaElectoral(numeroMesa);
            listaGeneral.AñadirFinal(nuevaMesa);
            JOptionPane.showMessageDialog(this, "Mesa electoral registrada exitosamente.");
            lbPresidente.setText("No asignado");
            lbSecretario.setText("No asignado");
            lbVocal.setText("No asignado");
            // Limpia los campos después del registro
            gestionRegistro();
        }
        
    }//GEN-LAST:event_btnRegistroActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        String numeroBuscar = this.txtBusca.getText();
        String modificar = this.txtNumeroMesa.getText();

        if (modificar.trim().isEmpty()) {
            // Mostrar mensaje si el campo modificar está vacío
            JOptionPane.showMessageDialog(this, "El campo de número de mesa no puede estar vacío.");
        } else if (listaGeneral.buscarNumeraMesa(numeroBuscar)) {
            // Si la mesa existe y el campo no está vacío, proceder con la modificación
            listaGeneral.modificarMesaElectoral(numeroBuscar, modificar);
            JOptionPane.showMessageDialog(this, "La mesa electoral: " + numeroBuscar + " ahora es: " + modificar);
        } else {
            // Si no se encuentra la mesa
            JOptionPane.showMessageDialog(this, "La Mesa no Existe");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        String numeroBuscar = this.txtBusca.getText();
        if (listaGeneral.buscarNumeraMesa(numeroBuscar)) {
            listaGeneral.elimanarMesaElectoral(numeroBuscar);
            JOptionPane.showMessageDialog(this, "La mesa electoral: " + numeroBuscar + "fue eliminada");
        } else {
            // Si no se encuentra, entonces modificar
            JOptionPane.showMessageDialog(this, "La Mesa no Existe");
        }
        limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

