    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        String dni = txtBuscar.getText().trim();

        // Verificar si el campo de DNI está vacío
        if (dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el DNI del miembro a modificar.");
            return;
        }

        // Verificar si el DNI tiene el formato correcto
        if (dni.length() != 8 || !dni.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos numéricos.");
            return;
        }

        // Buscar el miembro en la pila
        MiembroMesa miembroExistente = mesaElectoral.getPilaMiebros().buscarMienbro(dni);

        if (miembroExistente != null) {
            // Si se encuentra el miembro, permitimos la modificación de los datos
            String nuevoNombre = txtNombre.getText().trim();
            String nuevoApellido = txtApellido.getText().trim();
            String nuevoDNI = txtDNI.getText().trim();
            String nuevaClaveAcceso = txtClave.getText().trim();
            String nuevoTipoMienbro = (String) cmbTipoMienbro.getSelectedItem();

            // Verificar que los nuevos datos no estén vacíos
            if (nuevoNombre.isEmpty() || nuevoApellido.isEmpty() || nuevaClaveAcceso.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
                return;
            }

            // Modificar los datos del miembro
            miembroExistente.setNombre(nuevoNombre);
            miembroExistente.setApellido(nuevoApellido);
            miembroExistente.setDni(nuevoDNI);
            miembroExistente.setClaveAcceso(nuevaClaveAcceso);
            miembroExistente.setTipoMienbro(nuevoTipoMienbro);

            limpiar();
            JOptionPane.showMessageDialog(this, "Datos del miembro modificados exitosamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró un miembro de mesa con el DNI ingresado.");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        String dni = txtBuscar.getText(); // Obtener el DNI ingresado en el campo de texto

        // Verificar que se ha ingresado un DNI
        if (dni == null || dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un DNI válido.");
            return;
        }

        // Intentar eliminar el miembro con el DNI dado en la pila de miembros de MesaElectoral
        if (mesaElectoral.getPilaMiebros().buscarDNI(dni)) {
            mesaElectoral.getPilaMiebros().eliminarMiembro(dni);
            JOptionPane.showMessageDialog(this, "Miembro eliminado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningún miembro con ese DNI.");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed

        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String dni = txtDNI.getText().trim();
        String claveAcceso = txtClave.getText().trim();
        String tipoMienbro = (String) cmbTipoMienbro.getSelectedItem();

        // Verifica si los campos están completos
        if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty() || claveAcceso.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
            return;
        }
        // Verifica si el DNI tiene el formato correcto
        if (dni.length() != 8 || !dni.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos numéricos.");
            return;
        }
        // Verifica si el DNI ya existe en la pila de miembros
        if (mesaElectoral.getPilaMiebros().buscarDNI(dni)) {
            JOptionPane.showMessageDialog(this, "Ya existe un miembro de mesa con este DNI.");
            return;
        }

        MiembroMesa nuevoMiembro = new MiembroMesa(nombre, apellido, dni, tipoMienbro, claveAcceso);

        if (mesaElectoral.getPilaMiebros().buscarTipoMiembro(nuevoMiembro)) {
            JOptionPane.showMessageDialog(this, "Ya existe un : " + nuevoMiembro.getTipoMienbro());
            return;
        }     
        // Verifica si es posible agregar más miembros
        if (mesaElectoral.puedeAgregarMiembro()) {
            mesaElectoral.agregarMiembro(nuevoMiembro);
            limpiar();
            JOptionPane.showMessageDialog(this, "Miembro registrado exitosamente.");
        } else {
            JOptionPane.showMessageDialog(this, "La mesa ya tiene el máximo de 3 miembros.");
        }

        
    }//GEN-LAST:event_btnRegistroActionPerformed

