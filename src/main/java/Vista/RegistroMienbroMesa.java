    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String dni = txtBuscar.getText().trim();

        // Verificar si el campo de DNI está vacío
        if (dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el DNI del miembro a modificar.");
            return;
        }

        // Verificar si el DNI tiene el formato correcto
        if (dni.length() != 8 || !dni.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos numéricos.");
            return;
        }
        // Buscar el miembro en la pila
        MiembroMesa miembroExistente = mesaElectoral.getPilaMiebros().buscarMienbro(dni);

        if (miembroExistente != null) {
            txtNombre.setText(miembroExistente.getNombre());
            txtApellido.setText(miembroExistente.getApellido());
            txtClave.setText(miembroExistente.getClaveAcceso());
            txtDNI.setText(miembroExistente.getDni());
            if ("Presidente".equals(miembroExistente.getTipoMienbro())) {
                cmbTipoMienbro.setSelectedIndex(0);
            } else if ("Secretario".equals(miembroExistente.getTipoMienbro())) {
                cmbTipoMienbro.setSelectedIndex(1);
            } else {
                cmbTipoMienbro.setSelectedIndex(2);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró un miembro de mesa con el DNI ingresado.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

